void	ft_rev_int_tab(int *tab, int size)

{
	int	aux[size];
	int	i;

	i = 0;
	while (i < size)
	{
		aux[size - 1 - i] = tab[i];
		i++;
	}
	i = 0;
	while (i < size)
	{
		tab[i] = aux[i];
		i++;
	}
}
