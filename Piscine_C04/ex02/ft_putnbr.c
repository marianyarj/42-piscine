#include <unistd.h>

void	ft_putnbr(int nb)
{
	char	n;

	if (nb == -2147483648)
	{
		write (1, "-2147483648", 11);
	}
	if (nb >= -2147483647 && nb <= 2147483647)
	{
		if (nb < 0)
		{
			write (1, "-", 1);
			nb *= -1;
		}
		if (nb > 9)
		{
			ft_putnbr(nb / 10);
			ft_putnbr(nb % 10);
		}
		else
		{
			n = nb + '0';
			write (1, &n, 1);
		}
	}
}
